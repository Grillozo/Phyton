#Read 3 numbers and define the highest and the lowest numbers.

n1 = float(input("Type the first number: "))
n2 = float(input("Type the second number: "))
n3 = float(input("Type the third number: "))

print ("The highest number entered was {}".format(max(n1,n2,n3)))
print ("The lowest number entered was {}".format(min(n1,n2,n3)))
